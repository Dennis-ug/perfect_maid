String jobType(int num) {
  switch (num) {
    case 0:
      {
        return 'Laundary Services';
      }
    case 1:
      {
        return 'Cleaning Services';
      }
    case 2:
      {
        return 'Maid Services';
      }
    case 3:
      {
        return 'Delivery Services';
      }
    case 4:
      {
        return 'Special Services';
      }

    default:
      {
        return '';
      }
  }
}
