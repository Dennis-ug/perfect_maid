String jobType(int num) {
  switch (num) {
    case 0:
      {
        return 'Laundary';
      }
    case 1:
      {
        return 'Cleaning services';
      }
    case 2:
      {
        return 'Maid services';
      }
    case 3:
      {
        return 'Delivery services';
      }
    case 4:
      {
        return 'Special hire';
      }

    default:
      {
        return '';
      }
  }
}
